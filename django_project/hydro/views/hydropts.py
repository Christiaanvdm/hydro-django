def get_square_geojson(x, y, size):
    try:
        y = float(y)
        x = float(x)
        size = float(size)
    except:
        y = 0
        x = 0
        size = 1
    ymax = y + size/2
    ymin = y - size/2
    xmax = x + size/2
    xmin = x - size/2
    coordinates = [
        [
            [
                [
                    xmin,
                    ymax
                ],
                [
                    xmax,
                    ymax
                ],
                [
                    xmax,
                    ymin
                ],
                [
                    xmin,
                    ymin
                ],
            ]
        ]
    ]
    result = get_my_geojson(coordinates)
    return result



